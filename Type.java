package Calculator;

/**
 * This class creates the "Type" enums.
 * 
 * @author Alin Mican ID:100947751
 *
 */
public enum Type {
  /**
   * This is a Number enum to allow the calculator what type it shall expect.
   */
  NUMBER,
  /**
   * This is a Symbol enum to allow the calculator what type it shall expect.
   */
  SYMBOL,
  /**
   * This is a String enum to allow the calculator what type it shall expect.
   */
  STRING,
  /**
   * This is a Invalid enum to allow the calculator what type it shall expect.
   */
  INVALID;

}
